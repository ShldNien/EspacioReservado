package com.daw.pruebas;

public class EjemploWhile {

	public static void main(String[] args) {
		int num;
		num = 0;
		while(num < 20) {
			num++;
			System.out.println("Contador : "+num);
		}
		int num2;
		num2 = 20;
		while(num2 > 0) {
			num2--;
			System.out.println("Contador : "+num2);
		}
	}

}
